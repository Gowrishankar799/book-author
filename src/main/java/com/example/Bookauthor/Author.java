package com.example.Bookauthor;

public class Author {
    private String name;
    private int age;
    private char genger;
    private float rating;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public char getGenger() {
        return genger;
    }

    public void setGenger(char genger) {
        this.genger = genger;
    }

    public float getRating() {
        return rating;
    }

    public void setRating(float rating) {
        this.rating = rating;
    }
}
